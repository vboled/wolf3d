int	ft_word_count(char const *s, char c)
{
	int i;

	i = 0;
	if (*s == '\0')
		return (1);
	while (*s != '\0')
	{
		while (*s == c)
			++s;
		if (*s != '\0')
		{
			++i;
			while (*s != c && *s != '\0')
				++s;
		}
	}
	if (i == 0)
		return (1);
	return (i);
}
